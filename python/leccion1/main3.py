# Comparación
a = 5
b = 3
resultado = a == b
print(resultado)

# Diferente
resultado = a != b
print(resultado)

# Mayor que
resultado = a > b
print(resultado)

# Mayor o igual que
resultado = a >= b
print(resultado)

# Menor que
resultado = a < b
print(resultado)

# Menor o igual que
resultado = a <= b
print(resultado)


# Operadores lógicos

# Operador and
a = True
b = True
resultado = a and b
print(resultado)

# Operador or
resultado = a or b
print(resultado)

# Operador not
resultado = not a
print(resultado)
