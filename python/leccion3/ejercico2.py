edad = int(input("Ingrese su edad: "))

if edad >= 0 and edad <= 9:
    print("La infancia es increíble y bella.")
elif edad >= 10 and edad <= 19:
    print("Tienes muchos cambios, mucho que estudiar.")
elif edad >= 20 and edad <= 29:
  print("Amor y comienza el trabajo")
elif edad >= 30 and edad <= 39:
  print("Familia, trabajo y proyectos")
elif edad >= 40 and edad <= 49:
  print("Cambios hormonales y psicológicos")
elif edad >= 50 and edad <= 59:
  print("Disfrutando los años y experiencia de vida")
elif edad >= 60 and edad <= 69:
  print("El cuerpo empieza a pasar factura, jubilación")
elif edad >= 70 and edad <= 79:
  print("Un milagro llegar hasta aquí")
elif edad >= 80 and edad <= 89:
  print("Estoy cansado jefe.")
else:
  print("La edad digitada no es la correcta o ya estás muerto.")